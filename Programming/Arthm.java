class Arthm
{
	public static void main(String args[])
		{
	char c1, c2;
	c1 ='B';
	c2 ='A';
	System.out.println("ADD" + (c1+c2));
	System.out.println("Subtract" + (c1-c2));
	System.out.println("Multiply" + (c1*c2));
	System.out.println("Divide" + (c1/c2));
	System.out.println("Modulus" + (c1%c2));	
	}
}